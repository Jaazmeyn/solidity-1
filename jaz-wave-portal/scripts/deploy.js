const main = async () => {
    const [deployer] = await hre.ethers.getSigners();
    const accountBalance = await deployer.getBalance();
  
    console.log("Deploying contracts with account: ", deployer.address);
    console.log("Account balance: ", accountBalance.toString());
  
    const networkingContractFactory = await hre.ethers.getContractFactory("NetworkingPortal");
    const networkingContract = await networkingContractFactory.deploy();
    await networkingContract.deployed();
  
    console.log("NetworkingPortal address: ", networkingContract.address);
  };
  
  const runMain = async () => {
    try {
      await main();
      process.exit(0);
    } catch (error) {
      console.log(error);
      process.exit(1);
    }
  };
  
  runMain();
  